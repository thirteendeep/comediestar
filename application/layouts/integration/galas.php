            <h1 class="title_page">
                Découvrez ce que sont <br>
                <strong>les galas Comédie Star</strong>
            </h1>
            <br>
            <p>
                Les Galas Comédie Star sont des spectacles d’humour qui sont présentés à l’Étoile du Quartier Dix30 mettant en vedette des humoristes de la relève, des vétérans de la scène et aussi des vedettes internationales. On peut dire que c’est un pot-pourri d’humoristes triés sur le volet. Aujourd’hui, la plupart d’entre eux ont déjà eu leur «one-man show» et/ou plusieurs apparitions au festival Juste Pour Rire. Sous la forme d’un Gala, un animateur présente tour à tour les invités. Ces derniers viennent rôder leur «one man show» ou encore un numéro exclusif qui sera présenté aux Galas Juste Pour Rire de Montréal ou Grand Rire Bleu de Québec.
            </p>
            <p>
                Les galas débutèrent en Octobre 2006 au Centre Socioculturel de Brossard. En partenariat avec la Ville de Brossard et Les Loisirs Notre-Dame de Bonsecours, trois galas firent déplacer près de 1000 personnes. Par la suite, avec la création de la nouvelle salle du Dix30 et l’effervescence qui se créa autour du projet, les galas se déplacèrent au fabuleux théâtre de l’Étoile. Dès lors, les galas commencèrent à prendre de l’ampleur en s’associant à de nouveaux commanditaires et en approchant différents clients corporatifs. Le soir du test ultime, CHAUD’humour – La Grande Première fit déplacer près de 800 personnes et tous les humoristes furent ovationnés. Un grand spectacle au niveau de la scène et une évolution flagrante au niveau technique. Le test fut passé haut la main.  
            </p>
            <p>
                Un grand succès qui permit au gala d’entrevoir de belles possibilités dans un futur rapproché. Dès lors, trois dates par année ont été accordées au Galas. Avec le soutien des médias locaux et du public sans cesse grandissant, les neuf dernières représentations reçurent près de 9000 personnes. Maintenant appelés Comédie Star, les Galas sont devenus un incontournable de la scène humoristique. Définitivement, un classique que personne ne veut manquer! 
            </p>
            <p>
                À cet effet, il est important de noter que les galas accomplissent également une mission sociale en remettant de l’argent aux loisirs de la ville et en utilisant des billets pour des levées de fonds chez différentes associations à but non lucratif.
            </p>
